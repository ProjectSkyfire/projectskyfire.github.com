    <div class="description">The Power of the community</div>

    <div> <p>SkyFire is an MMORPG framework that's built using C++, and using a back-end of MySQL for the DB content. SkyFire was at one point a merger of Trinity & MaNGOS with many patches submitted by users, that is no longer the case. SkyFire is an individual project with a diverse development team, We love to hear from our users too, whether you want to give words of encouragment or <b>if you have code for us to merge into the repo (please <a href="https://github.com/ProjectSkyfire/SkyFireEMU/pulls">file a pull request</a> if you do, we love it when users contribute</b> :) ). SkyFire has been cleaned up a lot, and will continue to be cleaned up and optimized over time. Please help us by posting issues, patches, and enhancement suggestions on the tracker.</p></div>
    
